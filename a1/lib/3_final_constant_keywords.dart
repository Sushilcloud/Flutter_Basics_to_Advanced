void main()
{
  final name="Raman"; // final variable
  // Role of final is to declare constant variable
  print(name);

  const name1="Raman"; // const variable
  // Role of const is to declare constant variable
  print(name1);

  var name2="Raman"; // var variable
  // Role of var is to declare variable
  print(name2);

  // difference between final and const is final is run time variable and const is compile time variable
  // when we will use final

  final names=["Sushil","Mishra","ravi","pankaj","pankaj1"];
  names.add("pankaj2");
  print(names);

  




}